import { handler } from './handler';

describe('aSCII Art: the drunken bishop algorithm', () => {
  it.each([
    [
      'fc:94:b0:c1:e5:b0:98:7c:58:43:99:76:97:ee:9f:b7',
      [
        '+---[CODINGAME]---+',
        '|       .=o.  .   |',
        '|     . *+*. o    |',
        '|      =.*..o     |',
        '|       o + ..    |',
        '|        S o.     |',
        '|         o  .    |',
        '|          .  . . |',
        '|              o .|',
        '|               E.|',
        '+-----------------+',
      ],
    ],
    [
      '51:8e:d2:95:25:73:8c:eb:da:c4:9c:49:e6:0e:a9:d3',
      [
        '+---[CODINGAME]---+',
        '|          ==o    |',
        '|       . =o+.    |',
        '|      . + ..     |',
        '|       . .+      |',
        '|        SO o     |',
        '|        o O      |',
        '|       o *       |',
        '|      o E o      |',
        '|       .         |',
        '+-----------------+',
      ],
    ],
    [
      '02:64:d6:eb:fd:ca:bc:10:79:92:6e:91:d8:b5:f3:fd',
      [
        '+---[CODINGAME]---+',
        '|    +.           |',
        '|   +  .          |',
        '|    .  ..        |',
        '|     +.= .       |',
        '|    ..X.S        |',
        '|     ..*.o .     |',
        '|      +  .. .    |',
        '|     . +  .  .   |',
        '|        =o    E  |',
        '+-----------------+',
      ],
    ],
    [
      'a8:6c:78:04:a2:23:00:45:58:f7:1b:bc:dc:11:0a:71',
      [
        '+---[CODINGAME]---+',
        '|.=+ +.E .        |',
        '|o  . = . .       |',
        '|o .   = .        |',
        '|o. . . * .       |',
        '|+   . = S        |',
        '|.. + .           |',
        '|  . =            |',
        '|   o             |',
        '|                 |',
        '+-----------------+',
      ],
    ],
    [
      '00:00:00:00:00:00:00:00:ff:ff:ff:ff:ff:ff:ff:ff',
      [
        '+---[CODINGAME]---+',
        '|%....            |',
        '| .   .           |',
        '|  .   .          |',
        '|   .   .         |',
        '|    .   S        |',
        '|     .           |',
        '|      .          |',
        '|       .         |',
        '|        ........E|',
        '+-----------------+',
      ],
    ],
  ])('receiving %s should output the correct result', (fingerprint, expected) => {
    expect.assertions(1);

    expect(handler(fingerprint)).toStrictEqual(expected);
  });
});
