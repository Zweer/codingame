// For each test, count passable cells and estimate max possible score
function analyzeMap(name: string, mapLines: string[]): void {
  const grid = parseMap(mapLines);
  const h = grid.length;
  const w = grid[0].length;

  let walls = 0;
  let passable = 0;
  let unknown = 0;

  for (let y = 0; y < h; y++) {
    for (let x = 0; x < w; x++) {
      const c = grid[y][x];
      if (c === '#') walls++;
      else if (c === '?') unknown++;
      else passable++; // '.', '_', '@', 'E'
    }
  }

  console.log(`\n=== ${name} (${w}x${h}) ===`);
  console.log(`Walls: ${walls}, Passable: ${passable}, Unknown: ${unknown}`);
  console.log(`Total cells: ${w * h}, Max explorable: ${passable + unknown}`);
  console.log(`Score if visit all passable: ${passable * 2}`);
}

